from abc import ABC
from enum import Enum
from typing import Dict, Optional

from dataclasses import dataclass
from nassl.ssl_client import OpenSslVersionEnum


class CipherSuiteScanResultEnum(Enum):
    ACCEPTED_BY_SERVER = 1
    REJECTED_BY_SERVER = 2
    UNKNOWN_ERROR = 3


@dataclass(frozen=True)
class CipherSuiteScanResult:
    """The result of testing a single SSL/TLS cipher suite against a specific server.

    Attributes:
        result: Whether the cipher suite was accepted by the server, or not.
        name: The cipher suite's RFC name.
        openssl_name: The cipher suite's OpenSSL name.
        ssl_version: The SSL/TLS version that was used to connect to the server.
        is_anonymous: True if the cipher suite is an anonymous cipher suite (ie. no server authentication).
        key_size: The key size of the cipher suite's algorithm in bits.
        post_tls_handshake_response: The server's response after completing the SSL/TLS handshake and
            sending a sample request. None if no sample request was sent or if should_send_request_after_tls_handshake
            was set to False in the scan command.
        error_message: The error returned if the TLS handshake was not successful or if an unknown error happened.
    """

    result: CipherSuiteScanResultEnum
    openssl_name: str
    ssl_version: OpenSslVersionEnum

    post_tls_handshake_response: Optional[str]
    error_message: Optional[str]

    @property
    def name(self) -> str:
        """OpenSSL uses a different naming convention than the corresponding RFCs.
        """
        return _OPENSSL_TO_RFC_NAMES_MAPPING[self.ssl_version][self.openssl_name]

    @property
    def is_anonymous(self) -> bool:
        return True if "anon" in self.name else False

    @property
    def key_size(self) -> int:
        return _RFC_NAME_TO_KEY_SIZE_MAPPING[self.name]


